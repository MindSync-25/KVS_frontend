import '../../domain/entities/app_user.dart';

class UserModel extends AppUser {
  const UserModel({
    required super.uid,
    required super.phone,
    required super.name,
    super.photoUrl,
    required super.role,
    required super.position,
    super.positionStatus = PositionStatus.pendingAssignment,
    required super.district,
    required super.taluk,
    required super.ward,
    required super.constituencyId,
    required super.referralCode,
    super.referredBy,
    required super.referralCount,
    required super.activityScore,
    required super.issuesReported,
    required super.issuesResolved,
    required super.eventsAttended,
    required super.badges,
    required super.isVerified,
    required super.isActive,
    required super.language,
    required super.createdAt,
    required super.lastActiveAt,
  });

  factory UserModel.fromMap(Map<String, dynamic> map) {
    return UserModel(
      uid: map['uid'] as String,
      phone: map['phone'] as String,
      name: map['name'] as String,
      photoUrl: map['photo_url'] as String?,
      role: UserRole.values.firstWhere(
        (e) => e.name == (map['role'] ?? 'public'),
        orElse: () => UserRole.public,
      ),
      position: PartyPosition.values.firstWhere(
        (e) => e.name == (map['position'] ?? 'member'),
        orElse: () => PartyPosition.member,
      ),
      positionStatus: PositionStatus.values.firstWhere(
        (e) => e.name == (map['position_status'] ?? 'pendingAssignment'),
        orElse: () => PositionStatus.pendingAssignment,
      ),
      district: map['district'] as String? ?? '',
      taluk: map['taluk'] as String? ?? '',
      ward: map['ward'] as String? ?? '',
      constituencyId: map['constituency_id'] as String? ?? '',
      referralCode: map['referral_code'] as String? ?? '',
      referredBy: map['referred_by'] as String?,
      referralCount: (map['referral_count'] as int?) ?? 0,
      activityScore: (map['activity_score'] as int?) ?? 0,
      issuesReported: (map['issues_reported'] as int?) ?? 0,
      issuesResolved: (map['issues_resolved'] as int?) ?? 0,
      eventsAttended: (map['events_attended'] as int?) ?? 0,
      badges: List<String>.from(map['badges'] as List? ?? []),
      isVerified: (map['is_verified'] as bool?) ?? false,
      isActive: (map['is_active'] as bool?) ?? true,
      language: map['language'] as String? ?? 'en',
      createdAt: DateTime.parse(map['created_at'] as String),
      lastActiveAt: DateTime.parse(map['last_active_at'] as String),
    );
  }

  Map<String, dynamic> toMap() => {
        'uid': uid,
        'phone': phone,
        'name': name,
        'photo_url': photoUrl,
        'role': role.name,
        'position': position.name,
        'position_status': positionStatus.name,
        'district': district,
        'taluk': taluk,
        'ward': ward,
        'constituency_id': constituencyId,
        'referral_code': referralCode,
        'referred_by': referredBy,
        'referral_count': referralCount,
        'activity_score': activityScore,
        'issues_reported': issuesReported,
        'issues_resolved': issuesResolved,
        'events_attended': eventsAttended,
        'badges': badges,
        'is_verified': isVerified,
        'is_active': isActive,
        'language': language,
        'created_at': createdAt.toIso8601String(),
        'last_active_at': lastActiveAt.toIso8601String(),
      };
}
